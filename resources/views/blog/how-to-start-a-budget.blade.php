            <h2>How to set targets using last month</h2>
            <p>If you are unsure what to set, use your last month as a reference. Look at what you spent in each category and round to a simple number. The goal is not accuracy. The goal is a starting point you can adjust.</p>
            <p>For example, if you spent around $1,720 on Needs, set a target of $1,700 or $1,750. Keep it simple and move forward.</p>

            <h2>What to do if you overshoot a category</h2>
            <p>Overshooting is common in the first few months. When it happens, do not rewrite your entire plan. Note the reason, adjust one category for next month, and keep going. The point is to learn, not to punish yourself.</p>

            <h2>Common beginner mistakes and how to avoid them</h2>
            <h3>Tracking everything all at once</h3>
            <p>This often leads to burnout. Start with the categories that matter most.</p>

            <h3>Trying to optimize too early</h3>
            <p>Optimization comes later. In the beginning, your only goal is to see the whole picture.</p>

            <h3>Judging yourself for deviations</h3>
            <p>Budgets are a tool, not a verdict. A shift in spending is a signal, not a failure.</p>

            <h2>Why manual-first tools help beginners</h2>
