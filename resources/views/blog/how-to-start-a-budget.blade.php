            <ul>
                <li>Day 1: set your three categories and soft targets.</li>
                <li>Day 3: add two or three key transactions.</li>
                <li>Day 5: glance at totals and note any surprises.</li>
                <li>Day 7: write one sentence about what you learned.</li>
